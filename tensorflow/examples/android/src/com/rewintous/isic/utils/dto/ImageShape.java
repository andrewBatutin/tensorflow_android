package com.rewintous.isic.utils.dto;

public class ImageShape {
    private final int width;
    private final int height;

    public ImageShape(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
